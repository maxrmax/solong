#include "get_next_line.h"
#include <stdio.h>

int	main(void)
{
	int		fd;
	char	*line;

	fd = open("lines.txt", O_RDONLY);
	if (fd == -1)
	{
		printf("Error\n");
		return (1);
	}
	while (fd)
	{
		line = get_next_line(fd);
		if (line == NULL)
			break ;
		printf("%s", line);
		free(line);
	}
	close(fd);
	return (0);
}
